// ─── Renderer ─────────────────────────────────────────────────────────────────

const HERSHEY_CAP_HEIGHT = 21  // units from top to baseline

/**
 * Render a text string as SVG path data.
 *
 * @param text      The text to render
 * @param x         Left edge X in mm
 * @param y         Top of text Y in mm (NOT baseline)
 * @param fontSize  Height of capital letters in mm
 * @returns SVG `d` attribute string (can contain multiple M/L subpaths)
 */
export function hersheyText(text: string, x: number, y: number, fontSize: number): string {
  const scale = fontSize / HERSHEY_CAP_HEIGHT
  const paths: string[] = []
  let cursorX = x

  for (const char of text) {
    const code = char.codePointAt(0) ?? 63  // fallback to '?'
    const glyph = GLYPHS[code] ?? GLYPHS[63]!

    const glyphX = cursorX - glyph.left * scale

    for (const stroke of glyph.strokes) {
      if (stroke.length < 1) continue
      let isFirst = true
      for (const [px, py] of stroke) {
        const sx = glyphX + px * scale
        const sy = y + py * scale
        paths.push(isFirst ? `M${f(sx)},${f(sy)}` : `L${f(sx)},${f(sy)}`)
        isFirst = false
      }
    }

    cursorX += (glyph.right - glyph.left) * scale
  }

  return paths.join(' ')
}

/**
 * Measure the width of a rendered text string in mm.
 */
export function hersheyTextWidth(text: string, fontSize: number): number {
  const scale = fontSize / HERSHEY_CAP_HEIGHT
  let width = 0
  for (const char of text) {
    const code = char.codePointAt(0) ?? 63
    const glyph = GLYPHS[code] ?? GLYPHS[63]!
    width += (glyph.right - glyph.left) * scale
  }
  return width
}

// Format a number for SVG path (2 decimal places, no trailing zeros)
function f(n: number): string { return parseFloat(n.toFixed(2)).toString() }
